  /*返回话题列表 */
  public static getTopList(): any {
    return this.topList;
  }
  /* 返回话题*/
  public static getTopic(id: Number): any {
    // console.log(`传入方法的id：${id}`);
    for (let i = 0; i < this.topList.length; i++) {
      // console.log(`tId=`+this.topList[i].tId);
      // console.log(`topList:`+this.topList[i]);
      /*id：Number */
      if (id == this.topList[i].tId) {
        return this.topList[i];
      }
    }
  }
  /*返回话题关联的评论 */
  public static getComment(cId: Number): any {
    for (let j = 0; j < this.topList.length; j++) {
      for (let i = 0; i < this.topList[j].cmtList.length; i++) {
        if(cId==this.topList[j].cmtList[i].cmtId){
          return this.topList[j].cmtList[i];
        }
      }
    }

  }
  /*获取参与的话题发表和评论 */
  public static getMyParti(name:String):any{
    let partiList:Array<Object>=[];
    this.topList.forEach((topic)=>{
      // if(topic.tName==name){
      //   partiList.push(topic);
      // }
      topic.tName==name?partiList.push(topic):'';
      topic.cmtList.forEach((comment)=>{
        // if(comment.cmtName==name){
        //   partiList.push(comment);
        // }
      comment.cmtName==name?partiList.push(comment):'';
      })
    });
    return partiList;
  }
